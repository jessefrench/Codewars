// Grasshopper - Messi Goals Function

// DESCRIPTION:
// Messi is a soccer player with goals in three leagues: LaLiga, Copa del Rey, and Champions. Complete the function to return his total number of goals in all three leagues.

// NOTE:
// The input will always be valid.

// EXAMPLE:
// 5, 10, 2  -->  17

// SOLUTION:
function goals (laLigaGoals, copaDelReyGoals, championsLeagueGoals){
    return laLigaGoals + copaDelReyGoals + championsLeagueGoals
}